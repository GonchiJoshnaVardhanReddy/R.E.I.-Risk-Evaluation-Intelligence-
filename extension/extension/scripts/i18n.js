  const englishDict = DICTIONARIES[DEFAULT_LANG];
  let activeLang = DEFAULT_LANG;
  let activeDict = englishDict;

  function resolveLanguage(raw) {
    const base = String(raw || "").toLowerCase().split("-")[0];
    return SUPPORTED_LANGS.includes(base) ? base : DEFAULT_LANG;
  }

  function getBrowserLanguage() {
    if (typeof chrome !== "undefined" && chrome.i18n?.getUILanguage) {
      return chrome.i18n.getUILanguage();
    }
    if (typeof navigator !== "undefined" && navigator.language) {
      return navigator.language;
    }
    return DEFAULT_LANG;
  }

  function setActiveLanguage(lang) {
    activeLang = lang;
    activeDict = DICTIONARIES[lang] || englishDict;
  }

  function getStoredLanguage() {
    if (typeof chrome === "undefined" || !chrome.storage?.local) {
      return Promise.resolve("");
    }
    return new Promise((resolve) => {
      chrome.storage.local.get(["rei_language"], (result) => {
        resolve(result?.rei_language || "");
      });
    });
  }

  async function initI18n() {
    const stored = await getStoredLanguage();
    const resolvedStored = SUPPORTED_LANGS.includes(stored) ? stored : "";
    const fallback = resolveLanguage(getBrowserLanguage());
    setActiveLanguage(resolvedStored || fallback);
    return activeLang;
  }

  function t(key) {
    const direct = activeDict?.[key];
    if (direct) return direct;
    const chromeMsg = typeof chrome !== "undefined" && chrome.i18n?.getMessage
      ? chrome.i18n.getMessage(key)
      : "";
    if (chromeMsg) return chromeMsg;
    const en = englishDict?.[key];
    return en || key;
  }

  /** Apply translations to all elements with data-i18n attribute */
  function applyI18nToDOM() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.dataset.i18n;
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.dataset.i18nPlaceholder;
      if (key) el.placeholder = t(key);
    });
  }

  async function setLanguage(lang) {
    const next = resolveLanguage(lang);
    setActiveLanguage(next);
    if (typeof chrome !== "undefined" && chrome.storage?.local) {
      await new Promise((resolve) => {
        chrome.storage.local.set({ rei_language: next }, resolve);
      });
    }
    applyI18nToDOM();
    return next;
  }

  function getActiveLanguage() {
    return activeLang;
  }

  globalThis.reiExtI18n = {
    SUPPORTED_LANGS,
    DEFAULT_LANG,
    resolveLanguage,
    initI18n,
    setLanguage,
    getActiveLanguage,
    applyI18nToDOM,
    t,
  };
